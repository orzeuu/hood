        <h3>Plan działania jest taki</h3>

        <ol class="list-group">
            <li class="list-group-item"> zamawiamy w tartaku dokładnie na wymiar przycięte drewno heblowane, a wybieramy je sami, na miejscu (aby: raz, nie trzeba było tego szlifować, dwa, docinać, a trzy, by było proste jak drut - żadnych zwichrzonych belek czy słupów)</li>
            <li class="list-group-item"> zamawiamy brukarza, aby najpierw dorobił ścieżkę koło drewutni, której brakowało (nie robimy tego sami (choć pewnie byśmy mogli), bo zajęłoby nam to za dużo czasu i mogłoby wyjść nierówno... aż tacy dobrzy nie jesteśmy:)</li>
            <li class="list-group-item"> pierwszy dzień prac - betonujemy kotwy w ziemi i wykładamy kamyki pod drewutnię</li>
            <li class="list-group-item"> drugi dzień prac (po odczekaniu tygodnia, aż kotwy się dobrze zabetonują) - wykonujemy drewnianą konstrukcję drewutni, w całości</li>
            <li class="list-group-item"> trzeci dzień prac - malujemy i wykładamy dach papą</li>
        </ol>
