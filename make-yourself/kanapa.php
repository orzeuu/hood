<div>
    <br>
    <h2>Stolik z palet – instrukcja krok po kroku</h2>
    <br>
    <ul>
        <li>
            <h3>Kroki 1-3. Projekt, obróbka i malowanie</h3>
            <p>Oprócz wygodnej kanapy na tarasie nie może zabraknąć stolika, na którym postawimy dzbanek z zimną lemoniadą czy misę ze świeżymi owocami. Przed przystąpieniem do pracy należy wstępnie naszkicować mebel i określić jego rozmiar, bazując na wymiarach pojedynczej palety. Możemy dostosować wysokość i długość mebla do stworzonej przez nas kanapy. Stoliczek może być niższy (1-2 palety) lub wyższy, jeśli nie chcemy schylać się z kanapy po postawione przedmioty; standardowy lub trochę dłuższy, jeśli stworzyliśmy narożnik lub długą kanapę.</p>
        </li>
        <li>
            <br>
            <h3>Krok 4. Mocowanie palet</h3>
            <p>Palety stawiamy jedną na drugą, tworząc stolik. Sprawdzamy, czy równo do siebie przylegają, a następnie mocujemy za pomocą śrub, płaskowników i kątowników.</p>
        </li>
        <li>
            <br>
            <h3>Krok 5. Wykończenie stolika</h3>
            <p>Stolik kawowy z palet możemy dopasować do swoich potrzeb. Jeśli boimy się zarysowań na podłodze tarasu, zamontujmy kółeczka do spodu stolika, dzięki którym mebel z łatwością przesuniemy w dowolne miejsce. Położenie hartowanego szkła na blacie podniesie estetykę stolika. Nie dość, że elegancko wykończy palety, to jeszcze ułatwi utrzymanie mebla w czystości.</p>
        </li>
    </ul>
</div>
